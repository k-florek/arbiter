CREATE TABLE seq_samples (ID INTEGER PRIMARY KEY AUTOINCREMENT,
    RUNID TEXT NOT NULL,
    ISOID TEXT UNIQUE NOT NULL,
    STATUSCODE TEXT NOT NULL,
    READ1 TEXT UNIQUE NOT NULL,
    READ2 TEXT UNIQUE NOT NULL,
    FASTQC1 TEXT,
    FASTQC2 TEXT,
    KRAKEN TEXT,
    SALTYPE TEXT,
    STREPTYPE TEXT,
    ECOLITYPE TEXT);
CREATE TABLE seq_runs (ID INTEGER PRIMARY KEY AUTOINCREMENT,
    MACHINE TEXT NOT NULL,
    DATE DATE NOT NULL,
    PATH TEXT UNIQUE NOT NULL,
    FASTQC TEXT,
    KRAKEN TEXT,
    AR TEXT);
CREATE TABLE seq_ar (ID INTEGER PRIMARY KEY AUTOINCREMENT,
    RUNID TEXT,
    ISOID TEXT,
    GENE TEXT,
    CONTIG TEXT,
    GSTART TEXT,
    GEND TEXT,
    COVERAGE TEXT,
    IDENTITY TEXT,
    DATABASE TEXT,
    ACCESSION TEXT,
    DESCRIPTION TEXT);
